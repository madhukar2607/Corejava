package anarghyacomm;

public class unary {

	public static void main(String[] args) {
		
	    int a = 85, b = 73;
	    int result1, result2;

	    System.out.println("Value of a: " + a);
	    
	    result1 = ++a;
	    System.out.println("increment: " + result1);

	    System.out.println("Value of b: " + b);

	    result2 = --b;
	    System.out.println("decrement: " + result2);
	      

	}

}