package anarghyacomm;

public class relational {

	public static void main(String[] args) {
		
      int x=20, y=40;
      
      System.out.println("x == y :" + (x==y));
      System.out.println("x != y :" + (x!=y));
      System.out.println("x > y :" + (x>y));
      System.out.println("x < y :" + (x<y));
      System.out.println("x <= y :" + (x<=y));
      System.out.println("x >= y :" + (x>=y));
	}

}
